//Switch
let numero=4;

let numeroTexto ="valor desconocido";

switch(numero){
    case 1:
        numeroTexto="uno";
        break;
    case 2:
        numeroTexto= "dos";
        break;
    case 3:
        numeroTexto="tres";
        break;
    default:
        numeroTexto="caso no encontrado"
        break;        
}
console.log(numeroTexto)
